import pandas as pd

# ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å fruit.csv
data = pd.read_csv('fruit.csv')

# ‡πÅ‡∏™‡∏î‡∏á‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡πÉ‡∏ô‡∏ï‡∏≤‡∏£‡∏≤‡∏á
print("üìã ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î (‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå):")
print(data.to_string(index=False))

# ‡πÅ‡∏™‡∏î‡∏á‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡πÅ‡∏ñ‡∏ß‡πÅ‡∏•‡∏∞‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå
num_rows, num_cols = data.shape
print(f"\nüìä ‡∏°‡∏µ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î {num_rows} ‡πÅ‡∏ñ‡∏ß ‡πÅ‡∏•‡∏∞ {num_cols} ‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå")

# -------------------------------
# üéØ ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ö‡∏≤‡∏á‡∏™‡πà‡∏ß‡∏ô‡∏Å‡πà‡∏≠‡∏ô Normalize
# -------------------------------

print("\nüéØ ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Å‡πà‡∏≠‡∏ô Normalize (‡πÄ‡∏â‡∏û‡∏≤‡∏∞ features ‡∏ó‡∏µ‡πà‡∏à‡∏∞ Normalize):")
print(data[['fruit_name', 'mass', 'width', 'height', 'color_score']].head())

# -------------------------------
# üîß ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå‡∏ó‡∏µ‡πà‡∏à‡∏∞ Normalize
# -------------------------------

features = data[['mass', 'width', 'height', 'color_score']]

# -------------------------------
# üîÅ Min-Max Normalize
# -------------------------------

normalized_data = (features - features.min()) / (features.max() - features.min())

# ‡πÄ‡∏û‡∏¥‡πà‡∏° fruit_name ‡∏Å‡∏•‡∏±‡∏ö‡∏°‡∏≤‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏î‡∏π‡∏ß‡πà‡∏≤‡πÅ‡∏ñ‡∏ß‡πÑ‡∏´‡∏ô‡∏Ñ‡∏∑‡∏≠‡∏ú‡∏•‡πÑ‡∏°‡πâ‡∏≠‡∏∞‡πÑ‡∏£
normalized_data['fruit_name'] = data['fruit_name']

# -------------------------------
# ‚úÖ ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡∏•‡∏±‡∏á Normalize (‡∏ó‡∏∏‡∏Å‡πÅ‡∏ñ‡∏ß)
# -------------------------------

print("\n‚úÖ ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡∏•‡∏±‡∏á Min-Max Normalize:")
print(normalized_data.to_string(index=False))

# ‡πÅ‡∏™‡∏î‡∏á‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡πÅ‡∏ñ‡∏ß‡πÅ‡∏•‡∏∞‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå‡∏Ç‡∏≠‡∏á normalized data
n_rows, n_cols = normalized_data.shape
print(f"\nüìê ‡∏ï‡∏≤‡∏£‡∏≤‡∏á Normalize ‡∏°‡∏µ {n_rows} ‡πÅ‡∏ñ‡∏ß ‡πÅ‡∏•‡∏∞ {n_cols} ‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå")
